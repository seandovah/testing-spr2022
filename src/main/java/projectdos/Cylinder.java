package projectdos;

public class Cylinder {

    int radius;
    int height;

    public Cylinder(int radius, int height) {
        super();
        this.radius = radius;
        this.height = height;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }


    public int getHeight() {
        return height;
    }

    public void setHeight() {
        this.height = height;
    }
}
